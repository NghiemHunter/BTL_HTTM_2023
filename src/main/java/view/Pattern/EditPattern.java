    private void btConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmActionPerformed
        // TODO add your handling code here:
        
        String content = tfContent.getText();
        String labelString = cbLabel.getSelectedItem().toString();
        pattern.setContent(content);
        pattern.setLabel(listLabel.get(cbLabel.getSelectedIndex()));
        PatternDAO patternDAO = new PatternDAO();
        try {
            if(patternDAO.checkExistedPattern(pattern)==false){
                patternDAO.updatePattern(pattern);
                JOptionPane.showMessageDialog(this, "Cập nhật mẫu thành công");
                ManagePattern mp = new ManagePattern(position);
                mp.setVisible(true);
                this.dispose();
                    
            } else {
                throw new ExistedUserException();
                
            }
        } catch (ExistedUserException e) {
            JOptionPane.showMessageDialog(this, "Nội dung mẫu đã được đăng ký. Nhập lại...");
            tfContent.requestFocus(); 
        }
        patternDAO.updatePattern(pattern);
    }//GEN-LAST:event_btConfirmActionPerformed

