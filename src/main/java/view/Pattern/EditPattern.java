    public EditPattern(Pattern pattern1) {
        initComponents();
        setLocationRelativeTo(this);
        pattern = pattern1;
        position = pattern1.getLabel().getRole();
        switch (position) {
            case "Product":
                tfQuanLyNhan.setText("Xem và sửa mẫu sản phẩm");
                break;
            case "Property":
                tfQuanLyNhan.setText("Xem và sửa mẫu thuộc tính");
                break;
            case "Quality":
                tfQuanLyNhan.setText("Xem và sửa mẫu chất lượng");
                break;
            default:
                throw new AssertionError();
        }
        
        LabelDAO labelDAO = new LabelDAO();
        listLabel = labelDAO.getListLabel(position);
        for(Label label : listLabel){
            cbLabel.addItem(label.getName());
        }
        
        cbLabel.setSelectedItem(pattern1.getLabel().getName());
        tfId.setText(pattern1.getId()+"");
        tfContent.setText(pattern1.getContent());
        tfContent.requestFocus();
    }

